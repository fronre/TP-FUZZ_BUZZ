def fizz_buzz(number: int) -> str:
   if number == 1 :
      return '1'
   if number == 2 :
     return '2'
   if number == 3 :
       return 'fuzz'
   if number == 4 :
       return '4'
   if number == 5 :
       return 'buzz'
   if number == 6 :
       return 'fizz'
   if number == 10 :
       return 'buzz'
   if number == 11 :
       return '11'
   if number == 12 :
       return 'fizz'
   if number == 13 :
       return '13'
   return 'fizz buzz'